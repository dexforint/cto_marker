# Модуль определения типов блоков документа
# Содержит перечисление всех возможных типов блоков, используемых в Marker

from enum import auto, Enum


class BlockTypes(str, Enum):
    """
    Перечисление типов блоков документа.
    Каждый тип представляет определенный элемент структуры документа.
    Используется для классификации и обработки различных частей документа.
    """
    
    # Базовые текстовые элементы
    # Строка текста - последовательность символов в одной строке
    Line = auto()
    # Фрагмент текста (span) - часть строки с определенным форматированием
    Span = auto()
    # Отдельный символ
    Char = auto()
    
    # Группировки элементов
    # Группа рисунков - контейнер для связанных изображений и диаграмм
    FigureGroup = auto()
    # Группа таблиц - контейнер для связанных таблиц
    TableGroup = auto()
    # Группа списка - контейнер для элементов списка
    ListGroup = auto()
    # Группа картинок - контейнер для изображений
    PictureGroup = auto()
    
    # Структурные элементы документа
    # Страница документа - корневой контейнер для одной страницы
    Page = auto()
    # Подпись к изображению или таблице
    Caption = auto()
    # Блок кода программирования
    Code = auto()
    # Рисунок или диаграмма
    Figure = auto()
    # Сноска - дополнительная информация внизу страницы
    Footnote = auto()
    # Форма для ввода данных
    Form = auto()
    # Математическое уравнение (блочное)
    Equation = auto()
    # Рукописный текст
    Handwriting = auto()
    # Текст с inline математическими формулами
    TextInlineMath = auto()
    # Элемент списка (маркированного или нумерованного)
    ListItem = auto()
    # Нижний колонтитул страницы
    PageFooter = auto()
    # Верхний колонтитул страницы
    PageHeader = auto()
    # Изображение/картинка
    Picture = auto()
    # Заголовок раздела (H1, H2, H3 и т.д.)
    SectionHeader = auto()
    # Таблица
    Table = auto()
    # Обычный текстовый параграф
    Text = auto()
    # Оглавление документа
    TableOfContents = auto()
    # Документ - корневой контейнер для всего документа
    Document = auto()
    # Сложный регион с mixed content (используется для LLM обработки)
    ComplexRegion = auto()
    # Ячейка таблицы
    TableCell = auto()
    # Библиографическая ссылка
    Reference = auto()

    def __str__(self):
        """Возвращает имя типа блока как строку."""
        return self.name
